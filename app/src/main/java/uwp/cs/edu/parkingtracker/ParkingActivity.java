/**
 * Represents a basic user activity of the application that is essentially
 * connects and instantiates portions of the application that deals with parking map and voting
 * on which lots are full.
 * */
 public class ParkingActivity extends FragmentActivity {

    // Instance variables begin
    private String drawerItems[] = {"Navigate", "Other", "Save Parking Spot"};
    private DrawerLayout mDrawerLayout;
    private ListView mDrawerList;
    private ActionBarDrawerToggle mDrawerToggle;
    private CharSequence mDrawerTitle;
    private CharSequence mTitle;
    private ProgressBar progress;
    private LatLng mSaveSpot;
    private DeviceListeners mDevice;

    // Service
    final Intent mServiceIntent = new Intent(this, ZoneService.class);

    protected DeviceListeners deviceListeners = null;
    protected MapTransform mapTransform;


    public DeviceListeners getDeviceListeners() {
        if (deviceListeners == null) {
            // Instantiate new device listener.
            deviceListeners = new DeviceListeners(this);
    // Attach listener to the refresh, expert, and other lots buttons.
    // findViewById(R.id.basic_user_refresh_button).setOnClickListener(deviceListeners);
    // findViewById(R.id.basic_user_expert_button).setOnLongClickListener(deviceListeners);
    // findViewById(R.id.basic_user_other_lots_button).setOnClickListener(deviceListeners);
        }
        return deviceListeners;
    }

    // Activity Methods
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        progress = (ProgressBar) findViewById(R.id.loadingProgress);
        progress.setMax(CONSTANTS.zones.size());
        // Google Analytics

        // Get tracker.
        Tracker t = ((ThisApp) getApplication()).getTracker();

        // Set screen name.
        t.setScreenName("ParkingActivity");

        // Send a screen view.
        t.send(new HitBuilders.ScreenViewBuilder()
                .setNewSession()
                .build());
        // Google Analytics

        setUpNavDrawer();

        // Setup device listeners.
        getDeviceListeners();

        //Service
        final Intent mServiceIntent = new Intent(this, ZoneService.class);

        // Timer
        final Handler timerHandler = new Handler();
        //server refresh timer
        Runnable timerRunnable = new Runnable() {

            @Override
            public void run() {
                startService(mServiceIntent);
                timerHandler.postDelayed(this, 30000);
            }
        };
        timerHandler.postDelayed(timerRunnable, 0);

        // Setup map.
        mapTransform = new MapTransform(ParkingActivity.this);
        mapTransform.setUpMap();

    }

    @Override
    protected void onPause() {
        super.onPause();
        // Unregister the listener when the application is paused
        LocalBroadcastManager.getInstance(this).unregisterReceiver(loadingStatus);
    }

    @Override
    protected void onResume() {
        super.onResume();
        // Register for the particular broadcast based on ACTION string
        IntentFilter filter = new IntentFilter(ZoneService.ACTION);
        LocalBroadcastManager.getInstance(this).registerReceiver(loadingStatus, filter);
    }

    @Override
    public boolean onPrepareOptionsMenu(Menu menu) {
        // If the nav drawer is open, hide action items related to the content view
// boolean drawerOpen = mDrawerLayout.isDrawerOpen(mDrawerList);
// menu.findItem(R.id.action_settings).setVisible(!drawerOpen);
        return super.onPrepareOptionsMenu(menu);
    }

    /**
     *  Inflate the menu; this adds items to the action bar if it is present.
     * */
    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.menu_basic_user, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
//        if (id == R.id.basic_user_action_settings) {
//            return true;
//        }

        //exit option
        if (id == R.id.exit) {
            android.os.Process.killProcess(android.os.Process.myPid());
            System.exit(1);
            return true;
        }
        if (mDrawerToggle.onOptionsItemSelected(item)) {
            return true;
        }
        return super.onOptionsItemSelected(item);
    }

    @Override
    protected void onPostCreate(Bundle savedInstanceState) {
        super.onPostCreate(savedInstanceState);
        // Sync the toggle state after onRestoreInstanceState has occurred.
        mDrawerToggle.syncState();
    }

    @Override
    public void onConfigurationChanged(Configuration newConfig) {
        super.onConfigurationChanged(newConfig);
        mDrawerToggle.onConfigurationChanged(newConfig);
    }

    /*
    * Changes the activity screen for experts
    * */
    public void goToExpert(View view) {
        Intent intent = new Intent(this, ExpertActivity.class);
        startActivity(intent);
    }

    public void showParkDialogFragment(String zID) {
        ParkDialogFragment parkDialogFragment = new ParkDialogFragment();
        parkDialogFragment.setListener(deviceListeners);
        parkDialogFragment.setzID(zID);
        parkDialogFragment.show(getSupportFragmentManager(), "map");
    }
    /*
    * Triggers a call to get the tapped zone from other methods
    *
    * */
    public void tapEvent(int x, int y) {
        String zInfo = mapTransform.getZoneTapped(x, y);
        if (zInfo != null) {
            showParkDialogFragment(zInfo);
        }

    }

    private void setUpNavDrawer() {
        // added nav drawer
        mTitle = mDrawerTitle = getTitle();
        mDrawerLayout = (DrawerLayout) findViewById(R.id.drawer_layout);
        mDrawerList = (ListView) findViewById(R.id.left_drawer);
        // Set the adapter for the list view
        mDrawerList.setAdapter(new ArrayAdapter<String>(this,
                R.layout.drawer_list_item, drawerItems));
        // Set the list's click listener
        mDrawerList.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                if (position == 0) {
                    Intent mIntent  = new Intent(ParkingActivity.this, NavigateActivity.class);
                    startActivity(mIntent);
                    finish();
                }
                if (position ==2){
                    //for this position 2 we are clicking on the button that says save parking spot
                    mDevice = getDeviceListeners();
                    //we have a new instance of the class Device listeners and we call the getDeviceListeners Method
                    Location location1 = mDevice.getLocation();
                    //we got a new location and we call the get location method with our instance of the Device listener class
                    mSaveSpot = new LatLng(location1.getLatitude(),location1.getLongitude());
                    //our mSaveSpot and we convert our location
                    mapTransform.attachParkingSpot(mSaveSpot);

                }
                //Toast.makeText(getApplicationContext(), "item " + position + "selected", Toast.LENGTH_LONG).show();
            }
        });
        mDrawerToggle = new ActionBarDrawerToggle(this, mDrawerLayout,
                R.drawable.ic_drawer, R.string.drawer_open, R.string.drawer_close) {

            /*
            *  Called when a drawer has settled in a completely closed state.
            * */
            public void onDrawerClosed(View view) {
                super.onDrawerClosed(view);
                getActionBar().setTitle(mTitle);
                invalidateOptionsMenu(); // creates call to onPrepareOptionsMenu()
            }

            /*
            * Called when a drawer has settled in a completely open state.
            * */
            public void onDrawerOpened(View drawerView) {
                super.onDrawerOpened(drawerView);
                getActionBar().setTitle(mDrawerTitle);
                invalidateOptionsMenu(); // creates call to onPrepareOptionsMenu()
            }
        };
        mDrawerLayout.setDrawerListener(mDrawerToggle);
        getActionBar().setDisplayHomeAsUpEnabled(true);
        getActionBar().setHomeButtonEnabled(true);
    }

//    //option for server progress
    private void showLoadingBar(boolean option) {
        View mapView = findViewById(R.id.mapLayout);

        ViewGroup.MarginLayoutParams mapParams = (ViewGroup.MarginLayoutParams)
                mapView.getLayoutParams();
        //show progress bar
        if (option) {
            mapParams.setMargins(0, 10, 0, 0);
            mapView.requestLayout();
            progress.setVisibility(View.VISIBLE);
        }
        else {
            mapParams.setMargins(0, 0, 0, 0);
            mapView.requestLayout();
            progress.setVisibility(View.GONE);
        }

    }

    /**
     * Custom BroadcastReceiver for loading zones, displaying progress and calling to refresh map
     * when complete
     */
    private BroadcastReceiver loadingStatus = new BroadcastReceiver() {
           @Override
        public void onReceive(Context context, Intent intent) {
               boolean complete = intent.getBooleanExtra(CONSTANTS.DATA_STATUS,false);
            if(!complete) {
                showLoadingBar(true);
                int status = intent.getIntExtra(CONSTANTS.DATA_AMOUNT,CONSTANTS.zones.size());
                if (status < CONSTANTS.zones.size()) {
                    //progress.setProgress(status);
                    //showLoadingBar(true);
                }
                if (status == CONSTANTS.zones.size()) {
                    //showLoadingBar(false);
                }
                progress.setProgress(status);
            }
            if (complete) {
                progress.setProgress(0);
                showLoadingBar(false);
                mapTransform.refreshMap();
            }
        }
    };

}
