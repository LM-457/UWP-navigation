    /**
     * setUpMap : Positions map to specified lot coordinates, lays out the parking lot zones, and
     *              adds the markers required for user interaction.
     * @param latitude
     * @param longitude
     * @param zoomFactor
     */
    public void setUpMap(double latitude, double longitude, float zoomFactor) {
        MapsInitializer.initialize(passedActivity);
        // makes the map focus on the Student Center parking lot.
        mMap.moveCamera(CameraUpdateFactory.newLatLngZoom(new LatLng(latitude, longitude), zoomFactor));

        // Layout Grid Lines
//        this.layoutGrid();

        // creates the markers from the database arrays and adds them to the map.
        if(this.passedActivity.getLat().size() != 0 || this.passedActivity.getLng().size() != 0
                || this.passedActivity.getColors().size() != 0)
            this.layoutMarkers();

        //add building markers
        for (Map.Entry<String, LatLng> entry : CONSTANTS.buildings.entrySet()) {
            String key = entry.getKey();
            LatLng value = entry.getValue();
            mMap.addMarker(new MarkerOptions().title(key).position(value).icon(BitmapDescriptorFactory.fromResource(R.drawable.university)));
        }

        //add parking markers
        for (Map.Entry<String, LatLng> entry : CONSTANTS.parkingLots.entrySet()) {
            String key = entry.getKey();
            LatLng value = entry.getValue();
            mMap.addMarker(new MarkerOptions().title(key).position(value).icon(BitmapDescriptorFactory.fromResource(R.drawable.parking)));
        }

        //TODO: attempt database connection, if successful, load fullness values for zones and adjust polygonoptions fill color based on fill
        //TODO: if no database connection, dont bother drawing zones?

        //add parking zones
        for (Zone zone : CONSTANTS.zones) {
            zone.setPolygon(mMap.addPolygon(zone.getPolygonOptions()));
        }


    }

    /**
     * layoutMarkers() : Creates markers and add the markers to the map passed fragment.
     *
     */
    private void layoutMarkers() {

        // Sets up each marker and gives it a corresponding color, title, and snippet.
        for (int i = 0; i < this.mOptions.size(); i++) {
            mOptions.add(new MarkerOptions()
                    .title("Zone " + (i + 1))
                    .position(new LatLng(passedActivity.getLat().get(i), passedActivity.getLng().get(i))));
            switch (passedActivity.getColors().get(i).intValue()) {
                case (0):
                    mOptions.get(i).icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_GREEN))
                            .snippet("Empty");
                    break;
                case (1):
                    mOptions.get(i).icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_YELLOW))
                            .snippet("Half Full");
                    break;
                case (2):
                    mOptions.get(i).icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_RED))
                            .snippet("Full");
                    break;
                default:
                    mOptions.get(i).icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_GREEN));
            }
        }


        attachMarkersToMap();
    }

    /**
     * Physically attach the markers to the google map fragment. Done in one batch to minimize
     *  interuption to the main UI Thread.
     *
     */
    private void attachMarkersToMap(){
        for(MarkerOptions markerOptions : mOptions)
        {
            mMap.addMarker(markerOptions);
        }
    }


